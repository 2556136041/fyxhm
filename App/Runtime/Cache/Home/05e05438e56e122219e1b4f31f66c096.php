        <p>
        深圳市福逸轩红木家具于2016年8月创建，是一家专业经营红木家具的零售企业，目前在深圳、东莞开有三家连锁店。主要经营大红酸枝（交趾黄檀

        ）、黑酸枝（阔叶黄檀）、缅甸花梨（大果紫檀）等纯正木材，坚持真材实料、精工细作，所销售红木家具全部为自产自销。</p>

        <p>福逸轩红木的掌舵人孙强勋曾就职于好百年家居、松宝大家具等华南知名家具连锁企业，浸淫行业十多年，精通家具行业专

        业知识，与全国红木家具各大原产地的工厂建立了紧密的合作关系。</p>

        <p>经过三年的发展沉淀，福逸轩红木家具在深圳红木家具市场奠定了坚实的基础，不仅与主要红木生产基地的优质厂商建立了长期的合作关系，确保了销售的都是真材实料的好红木，口碑随着客户数量的增长不断提升，“买好红木，认准福逸轩红木”的品牌影响力深入人心。</p>
        <p>福逸轩红木家具不仅重视品牌、质量、服务建设，而且还积极拥抱互联网，创建了自己的官方网站、官方微信、小程序等自媒体，搭建了一个全方位的新媒体矩阵，顾客不仅可以通过电脑、手机等多渠道了解福逸轩红木的产品、活动、及时沟通送货安装等问题 。这样做的目的在于更好地拉近与顾客的距离。在以传统经营模式为主的红木零售行业，福逸轩红木走在了行业前列。</p>

        <p>福逸轩红木家具馆在产品定位上经历了多次变化和调整，坚持“精品”发展路线，主营缅甸花梨木、印尼酸枝等。为满足个性化需求，还可以根据顾客的要求实行全屋订制。价格方面，继续实行“亲民价”，做到同样的材质价格上有优势，同样的价格工艺上有优势，让顾客买到称心如意的红木家具</p>
        <p>福逸轩红木家具网是其立足工厂打造官方网上展示平台。除了展示企业的文化和产品外，还为消费者提供了"选购"、"

        保养"。</p>

        <p>作为一个八零后创业者，孙强勋还是一个热心于公益事业的爱心人士，总是主动向困难人士伸出援手，曾援助过一批生活困难的家庭和留守儿童。</p>

