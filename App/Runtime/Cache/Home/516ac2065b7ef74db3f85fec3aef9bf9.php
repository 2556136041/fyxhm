<p id="p_lead" style="margin-top: 10px;">
在"六一"儿童节来临之际，深圳福逸轩红木家具走进贵州省遵义市务川县都濡镇桐木村，探望留守儿童，为他们带去了一份份包含着关受的节日礼物：书包、文具盒以及学习用具。怀抱着这些崭新精美的节日礼物，这些天真纯朴的孩子表现出爱不释手，脸上绽放着动人的笑容。
</p>
<div id="content">
<p style="padding: 5px 20px;">桐木村位于都濡镇的南面，距务川县城12公里。同中国的许多村庄一样，青壮年劳动力都常年在外面务工，村里只有老人和青少年儿童在家留守。该村小学共有六七十名学生，由于为村级小

学，教学环境和基础设施都不尽完善，一些孩子甚至都没有一个像样的书包，穿的衣服鞋子都很破旧。当福逸轩公司得知这一情况后，专门联系到该学校的负责人，决定购买一批

新书包和文具送给孩子们。不久前，这些带着关爱的节日礼物经过福逸轩员工的手中送到了每一个孩子的手中，看着那一张张开心的脸，令每一个福逸轩的员工都感到无比欣慰。
</p>
